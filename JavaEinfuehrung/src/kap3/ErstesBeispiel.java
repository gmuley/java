package kap3;

public class ErstesBeispiel {
	public static void main(String[] args) {
		// Variablendeklaration
		int ersteZahl;
		int zweiteZahl;
		int ergebnis;
		
		// Variableninitialisierung (Wertzuweisung)
		ersteZahl = 8754;
		zweiteZahl = 398;
		
		// Verwendung (Rechnung)
		ergebnis = ersteZahl - zweiteZahl;
		
		// Ausgabe auf Screen
		System.out.println("8754 - 398 = " + ergebnis);
	}

}
